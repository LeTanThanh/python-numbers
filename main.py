if __name__ == "__main__":
  # Integers
  print(20 + 10)        # 30
  print(20 - 10)        # 10
  print(20 * 10)        # 200
  print(20 / 10)        # 2.0
  print(3 ** 3)         # 27
  print(20 / (10 + 10)) # 1.0

  # Floats
  print(0.5 + 0.5)  # 1.0
  print(0.5 - 0.5)  # 0.0
  print(0.5 / 0.5)  # 1.0
  print(0.5 * 0.5)  # 0.25
  print(20 / 10)    # 2.0
  print(1 + 2.0)    # 3.0
  print(0.1 + 0.2)  # 0.30000000000000004

  # Underscores in numbers
  count = 10_000_000_000
  print(count)
